    func name() -> String {
        var red: CGFloat = 0
        var green: CGFloat = 0
        var blue: CGFloat = 0
        getRed(&red, green: &green, blue: &blue, alpha: nil)

        let sortedNames = UIColor.names.sorted { (color1Info, color2Info) -> Bool in
            let color1 = color1Info.value.rgba()
            let color1redDiff = abs(red - color1.red)
            let color1greenDiff = abs(green - color1.green)
            let color1blueDiff = abs(blue - color1.blue)
            let total1Diff = color1redDiff + color1greenDiff + color1blueDiff

            let color2 = color2Info.value.rgba()
            let color2redDiff = abs(red - color2.red)
            let color2greenDiff = abs(green - color2.green)
            let color2blueDiff = abs(blue - color2.blue)
            let total2Diff = color2redDiff + color2greenDiff + color2blueDiff

            return total1Diff < total2Diff
        }

        guard let firstColor = sortedNames.first else { fatalError("Colors should contain at least 1 value") }
        return firstColor.key
    }
